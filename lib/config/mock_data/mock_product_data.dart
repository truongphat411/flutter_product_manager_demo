  static void delete(int id) {
    _products.removeWhere((p) => p.id == id);
  }

  static ProductModel getById(int id) {
    return _products.firstWhere(
      (p) => p.id == id,
      orElse: () => throw Exception('Sản phẩm không tồn tại'),
    );
  }

  static void addImage(int productId, String imageUrl) {
    final index = _products.indexWhere((p) => p.id == productId);
    if (index != -1) {
      final product = _products[index];
      final updatedImages = [...product.images, imageUrl];
      _products[index] = product.copyWith(images: updatedImages);
    }
  }
